    private void btnJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJogarActionPerformed
        String sql = "", nomeJogador1,nomeJogador2;
        nomeJogador1 = tfJogador1.getText().toUpperCase();
        nomeJogador2 = tfJogador2.getText().toUpperCase();
        boolean acheiJogador1 = false;
        boolean acheiJogador2 = false;
             
        try{
            if(nomeJogador1.equals("") || nomeJogador2.equals(""))
            {
                JOptionPane.showMessageDialog(null, "Escreva o nome dos jogadores corretamente!"," ERRO",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                sql = "SELECT * FROM jogadores where NomeJogador='" + nomeJogador1 + "' OR NomeJogador='" + nomeJogador2 + "'";
                Statement stm = con.prepareStatement(sql);
                stm.execute(sql);
                ResultSet rs = stm.executeQuery(sql);
                while(rs.next())
                {
                    if ( rs.getString("NomeJogador").toUpperCase().equals(nomeJogador1))
                    {
                        Variaveis.vitoriasJogador1 = Integer.parseInt(rs.getString("NumVitoria"));
                        Variaveis.nomeJogador1 =  rs.getString("NomeJogador");
                        acheiJogador1 = true;
                    }
                    else if ( rs.getString("NomeJogador").toUpperCase().equals(nomeJogador2))
                    {
                        Variaveis.vitoriasJogador2  = Integer.parseInt(rs.getString("NumVitoria"));
                        Variaveis.nomeJogador2 =  rs.getString("NomeJogador");
                        acheiJogador2 = true;
                    }
                }

                if(!acheiJogador1)
                {
                    sql="INSERT INTO Jogadores (NomeJogador, NumVitoria) values ('" + nomeJogador1 +"',0 )";
                    stm = con.prepareStatement(sql);
                    stm.execute(sql);
                    Variaveis.vitoriasJogador1 = 0;
                    Variaveis.nomeJogador1 =  nomeJogador1;
                }
                
                if(!acheiJogador2)
                {
                    sql="INSERT INTO Jogadores (NomeJogador, NumVitoria) values ('" + nomeJogador2 +"',0 )";
                    stm = con.prepareStatement(sql);
                    stm.execute(sql);
                   
                    Variaveis.vitoriasJogador2 = 0;
                    Variaveis.nomeJogador2 =  nomeJogador2;
                }
                
                tJogo tJogo = new tJogo();
                tJogo.setVisible(true);
                this.setVisible(false);
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro ao incluir, tente novamente" + e,"ERRO",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnJogarActionPerformed

